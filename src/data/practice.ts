export const KEY_LOOKUP = KEYBOARD_ROWS.flat().reduce<
  Record<string, { code: string; shifted: boolean; finger: FingerId }>
>((lookup, key) => {
  lookup[key.hangul] = { code: key.code, shifted: false, finger: key.finger };
  if (key.shifted) {
    lookup[key.shifted] = { code: key.code, shifted: true, finger: key.finger };
  }
  return lookup;
}, {});

export function getCumulativeKeys(stageIndex: number) {
  return PRACTICE_STAGES.slice(0, stageIndex + 1).flatMap((stage) => stage.keys);
}

export function decomposeHangulToKeys(text: string) {
  const keys: string[] = [];

  for (const char of text) {
    const code = char.charCodeAt(0);

    if (code >= 0xac00 && code <= 0xd7a3) {
      const offset = code - 0xac00;
      const leading = Math.floor(offset / 588);
      const vowel = Math.floor((offset % 588) / 28);
      const trailing = offset % 28;
      keys.push(...expandJamo(LEADING[leading]));
      keys.push(...expandJamo(VOWELS[vowel]));
      if (TRAILING[trailing]) {
        keys.push(...expandJamo(TRAILING[trailing]));
      }
      continue;
    }

    if (/^[ㄱ-ㅎㅏ-ㅣ]$/.test(char)) {
      keys.push(...expandJamo(char));
    }
  }

  return keys;
}

export function getWordsForStage(stageIndex: number) {
  const allowed = new Set(getCumulativeKeys(stageIndex).filter((key) => key !== ";"));

  return [...new Set(WORD_BANK)].filter((word) =>
    decomposeHangulToKeys(word).every((key) => allowed.has(key)),
  );
}

export function getTextsForStage(stageIndex: number) {
  const allowed = new Set(getCumulativeKeys(stageIndex).filter((key) => key !== ";"));

  return [...new Set(SHORT_TEXT_BANK)].filter((text) =>
    decomposeHangulToKeys(text).every((key) => allowed.has(key)),
  );
}

function expandJamo(jamo: string) {
  return COMPOSED_TO_KEYS[jamo] ?? [jamo];
}
